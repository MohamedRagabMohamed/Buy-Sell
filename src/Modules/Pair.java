package Modules;

public class Pair {
	public String first,second;

	public Pair(String first, String Second) {
		super();
		this.first = first;
		second = Second;
	}

	public String getFirst() {
		return first;
	}

	public void setFirst(String first) {
		this.first = first;
	}

	public String getSecond() {
		return second;
	}

	public void setSecond(String Second) {
		second = Second;
	}
	
}
